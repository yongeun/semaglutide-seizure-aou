library(bigrquery)
library(tidyverse)
library(glue)

# --- Setup ---
dataset        <- Sys.getenv("WORKSPACE_CDR")
google_project <- Sys.getenv("GOOGLE_PROJECT")

download_data <- function(sql, google_project = NULL) {
  if (is.null(google_project)) google_project <- Sys.getenv("GOOGLE_PROJECT")
  bq_table_download(bq_project_query(google_project, sql))
}

